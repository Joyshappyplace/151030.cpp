//A C++ program to compute the area of a rectangle
#include <iostream>
using namespace std;

int main() {
    //Variable declaration
    int area, length, width;
    //variable initialization
    length=8;
    width=5;
    //compute the area
    area=length*width;
    //output the area
    cout<<"Area of the rectangle is: "<<area;
    return 0;
}